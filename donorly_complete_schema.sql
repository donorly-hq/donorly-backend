-- =============================================================================
-- Donorly – Complete Database Schema (fresh install)
-- =============================================================================
-- This script creates the entire Donorly database from scratch.
-- Run against an empty PostgreSQL database owned by the application user.
--
-- Audit columns present on every table:
--   created_at  TIMESTAMPTZ  — when the row was first inserted
--   created_by  UUID         — user who created the row
--   modified_at TIMESTAMPTZ  — when the row was last updated
--   modified_by UUID         — user who last updated the row
--
-- Convention:
--   • All primary keys are UUID generated by gen_random_uuid().
--   • Every tenant-owned table carries organization_id.
--   • Status / type columns use VARCHAR CHECK constraints.
--   • Soft-deletes use deleted_at TIMESTAMPTZ (NULL = alive).
-- =============================================================================

CREATE EXTENSION IF NOT EXISTS pgcrypto;

-- ============================================================
-- PHASE 0 — Platform, tenants, identity, RBAC
-- ============================================================

CREATE TABLE organizations (
    id              UUID         PRIMARY KEY DEFAULT gen_random_uuid(),
    name            VARCHAR(200) NOT NULL,
    slug            VARCHAR(120) NOT NULL UNIQUE,
    vertical        VARCHAR(50)  NOT NULL DEFAULT 'nonprofit',
    status          VARCHAR(30)  NOT NULL DEFAULT 'trial'
                        CHECK (status IN ('trial','active','suspended','cancelled')),
    logo_url        TEXT,
    primary_color   VARCHAR(20),
    timezone        VARCHAR(80)  NOT NULL DEFAULT 'America/Chicago',
    deleted_at      TIMESTAMPTZ,
    -- audit
    created_at      TIMESTAMPTZ  NOT NULL DEFAULT now(),
    created_by      UUID,
    modified_at     TIMESTAMPTZ  NOT NULL DEFAULT now(),
    modified_by     UUID
);

CREATE TABLE organization_settings (
    organization_id UUID         PRIMARY KEY REFERENCES organizations(id),
    receipt_prefix  VARCHAR(20),
    default_currency VARCHAR(3)  NOT NULL DEFAULT 'USD',
    ai_enabled      BOOLEAN      NOT NULL DEFAULT false,
    payment_enabled BOOLEAN      NOT NULL DEFAULT false,
    -- audit
    created_at      TIMESTAMPTZ  NOT NULL DEFAULT now(),
    created_by      UUID,
    modified_at     TIMESTAMPTZ  NOT NULL DEFAULT now(),
    modified_by     UUID
);

CREATE TABLE users (
    id                   UUID         PRIMARY KEY DEFAULT gen_random_uuid(),
    email                VARCHAR(255) NOT NULL,
    full_name            VARCHAR(200) NOT NULL,
    phone                VARCHAR(30),
    password_hash        TEXT,
    status               VARCHAR(30)  NOT NULL DEFAULT 'active'
                             CHECK (status IN ('invited','active','disabled')),
    is_platform_admin    BOOLEAN      NOT NULL DEFAULT false,
    active_session_token VARCHAR(100),
    last_login_at        TIMESTAMPTZ,
    -- audit
    created_at           TIMESTAMPTZ  NOT NULL DEFAULT now(),
    created_by           UUID,
    modified_at          TIMESTAMPTZ  NOT NULL DEFAULT now(),
    modified_by          UUID
);
CREATE UNIQUE INDEX uq_users_email ON users (lower(email));

-- Self-referencing FK for created_by/modified_by added after table exists
ALTER TABLE organizations        ADD CONSTRAINT fk_orgs_created_by  FOREIGN KEY (created_by)  REFERENCES users(id);
ALTER TABLE organizations        ADD CONSTRAINT fk_orgs_modified_by FOREIGN KEY (modified_by) REFERENCES users(id);
ALTER TABLE organization_settings ADD CONSTRAINT fk_org_settings_created_by  FOREIGN KEY (created_by)  REFERENCES users(id);
ALTER TABLE organization_settings ADD CONSTRAINT fk_org_settings_modified_by FOREIGN KEY (modified_by) REFERENCES users(id);
ALTER TABLE users ADD CONSTRAINT fk_users_created_by  FOREIGN KEY (created_by)  REFERENCES users(id);
ALTER TABLE users ADD CONSTRAINT fk_users_modified_by FOREIGN KEY (modified_by) REFERENCES users(id);

CREATE TABLE roles (
    id          UUID         PRIMARY KEY DEFAULT gen_random_uuid(),
    code        VARCHAR(80)  NOT NULL UNIQUE,
    name        VARCHAR(120) NOT NULL,
    scope       VARCHAR(30)  NOT NULL DEFAULT 'organization'
                    CHECK (scope IN ('platform','organization')),
    is_system   BOOLEAN      NOT NULL DEFAULT true,
    -- audit
    created_at  TIMESTAMPTZ  NOT NULL DEFAULT now(),
    created_by  UUID         REFERENCES users(id),
    modified_at TIMESTAMPTZ  NOT NULL DEFAULT now(),
    modified_by UUID         REFERENCES users(id)
);

CREATE TABLE permissions (
    id          UUID         PRIMARY KEY DEFAULT gen_random_uuid(),
    code        VARCHAR(120) NOT NULL UNIQUE,
    description VARCHAR(255),
    -- audit
    created_at  TIMESTAMPTZ  NOT NULL DEFAULT now(),
    created_by  UUID         REFERENCES users(id),
    modified_at TIMESTAMPTZ  NOT NULL DEFAULT now(),
    modified_by UUID         REFERENCES users(id)
);

CREATE TABLE role_permissions (
    role_id       UUID NOT NULL REFERENCES roles(id),
    permission_id UUID NOT NULL REFERENCES permissions(id),
    PRIMARY KEY (role_id, permission_id),
    -- audit
    created_at    TIMESTAMPTZ NOT NULL DEFAULT now(),
    created_by    UUID        REFERENCES users(id),
    modified_at   TIMESTAMPTZ NOT NULL DEFAULT now(),
    modified_by   UUID        REFERENCES users(id)
);

CREATE TABLE organization_memberships (
    id              UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
    organization_id UUID        NOT NULL REFERENCES organizations(id),
    user_id         UUID        NOT NULL REFERENCES users(id),
    role_id         UUID        NOT NULL REFERENCES roles(id),
    status          VARCHAR(30) NOT NULL DEFAULT 'active'
                        CHECK (status IN ('invited','active','disabled')),
    UNIQUE (organization_id, user_id),
    -- audit
    created_at      TIMESTAMPTZ NOT NULL DEFAULT now(),
    created_by      UUID        REFERENCES users(id),
    modified_at     TIMESTAMPTZ NOT NULL DEFAULT now(),
    modified_by     UUID        REFERENCES users(id)
);
CREATE INDEX idx_memberships_user ON organization_memberships (user_id);
CREATE INDEX idx_memberships_org  ON organization_memberships (organization_id);

CREATE TABLE invitations (
    id              UUID         PRIMARY KEY DEFAULT gen_random_uuid(),
    organization_id UUID         NOT NULL REFERENCES organizations(id),
    email           VARCHAR(255) NOT NULL,
    role_id         UUID         NOT NULL REFERENCES roles(id),
    token_hash      VARCHAR(255) NOT NULL UNIQUE,
    status          VARCHAR(30)  NOT NULL DEFAULT 'pending'
                        CHECK (status IN ('pending','accepted','revoked','expired')),
    expires_at      TIMESTAMPTZ  NOT NULL,
    accepted_at     TIMESTAMPTZ,
    -- audit
    created_at      TIMESTAMPTZ  NOT NULL DEFAULT now(),
    created_by      UUID         REFERENCES users(id),
    modified_at     TIMESTAMPTZ  NOT NULL DEFAULT now(),
    modified_by     UUID         REFERENCES users(id)
);
CREATE INDEX idx_invitations_org ON invitations (organization_id);

-- ============================================================
-- PHASE 1 — Donors
-- ============================================================

CREATE TABLE donors (
    id              UUID         PRIMARY KEY DEFAULT gen_random_uuid(),
    organization_id UUID         NOT NULL REFERENCES organizations(id),
    full_name       VARCHAR(200) NOT NULL,
    email           VARCHAR(255),
    phone           VARCHAR(30),
    city            VARCHAR(120),
    donor_type      VARCHAR(30)  NOT NULL DEFAULT 'individual'
                        CHECK (donor_type IN ('individual','family','business','anonymous')),
    status          VARCHAR(30)  NOT NULL DEFAULT 'active'
                        CHECK (status IN ('active','inactive','do_not_contact','merged')),
    lifetime_giving NUMERIC(14,2) NOT NULL DEFAULT 0,
    deleted_at      TIMESTAMPTZ,
    -- audit
    created_at      TIMESTAMPTZ  NOT NULL DEFAULT now(),
    created_by      UUID         REFERENCES users(id),
    modified_at     TIMESTAMPTZ  NOT NULL DEFAULT now(),
    modified_by     UUID         REFERENCES users(id)
);
CREATE INDEX idx_donors_org_name   ON donors (organization_id, full_name);
CREATE UNIQUE INDEX uq_donors_org_email ON donors (organization_id, lower(email)) WHERE email IS NOT NULL;

CREATE TABLE donor_profiles (
    donor_id           UUID         PRIMARY KEY REFERENCES donors(id),
    organization_id    UUID         NOT NULL REFERENCES organizations(id),
    occupation         VARCHAR(200),
    employer           VARCHAR(200),
    preferred_language VARCHAR(50),
    preferred_channel  VARCHAR(30),
    notes_private      TEXT,
    -- audit
    created_at         TIMESTAMPTZ  NOT NULL DEFAULT now(),
    created_by         UUID         REFERENCES users(id),
    modified_at        TIMESTAMPTZ  NOT NULL DEFAULT now(),
    modified_by        UUID         REFERENCES users(id)
);

CREATE TABLE donor_tags (
    id              UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
    organization_id UUID        NOT NULL REFERENCES organizations(id),
    name            VARCHAR(80) NOT NULL,
    color           VARCHAR(20),
    UNIQUE (organization_id, name),
    -- audit
    created_at      TIMESTAMPTZ NOT NULL DEFAULT now(),
    created_by      UUID        REFERENCES users(id),
    modified_at     TIMESTAMPTZ NOT NULL DEFAULT now(),
    modified_by     UUID        REFERENCES users(id)
);

CREATE TABLE donor_tag_assignments (
    donor_id    UUID NOT NULL REFERENCES donors(id),
    tag_id      UUID NOT NULL REFERENCES donor_tags(id),
    PRIMARY KEY (donor_id, tag_id),
    -- audit
    created_at  TIMESTAMPTZ NOT NULL DEFAULT now(),
    created_by  UUID        REFERENCES users(id),
    modified_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    modified_by UUID        REFERENCES users(id)
);

CREATE TABLE donor_notes (
    id              UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
    organization_id UUID        NOT NULL REFERENCES organizations(id),
    donor_id        UUID        NOT NULL REFERENCES donors(id),
    note_text       TEXT        NOT NULL,
    note_type       VARCHAR(40),
    visibility      VARCHAR(20) NOT NULL DEFAULT 'team'
                        CHECK (visibility IN ('private','team')),
    -- audit
    created_at      TIMESTAMPTZ NOT NULL DEFAULT now(),
    created_by      UUID        REFERENCES users(id),
    modified_at     TIMESTAMPTZ NOT NULL DEFAULT now(),
    modified_by     UUID        REFERENCES users(id)
);
CREATE INDEX idx_donor_notes_donor ON donor_notes (donor_id);

-- ============================================================
-- PHASE 1 — Campaigns
