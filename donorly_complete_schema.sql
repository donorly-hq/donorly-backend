CREATE TABLE campaigns (
    id              UUID          PRIMARY KEY DEFAULT gen_random_uuid(),
    organization_id UUID          NOT NULL REFERENCES organizations(id),
    name            VARCHAR(200)  NOT NULL,
    slug            VARCHAR(120)  NOT NULL,
    campaign_type   VARCHAR(50)   NOT NULL DEFAULT 'general',
    goal_amount     NUMERIC(14,2) NOT NULL DEFAULT 0 CHECK (goal_amount >= 0),
    start_date      DATE,
    end_date        DATE,
    status          VARCHAR(30)   NOT NULL DEFAULT 'draft'
                        CHECK (status IN ('draft','active','paused','completed','archived')),
    UNIQUE (organization_id, slug),
    -- audit
    created_at      TIMESTAMPTZ   NOT NULL DEFAULT now(),
    created_by      UUID          REFERENCES users(id),
    modified_at     TIMESTAMPTZ   NOT NULL DEFAULT now(),
    modified_by     UUID          REFERENCES users(id)
);
CREATE INDEX idx_campaigns_org ON campaigns (organization_id);

CREATE TABLE campaign_milestones (
    id              UUID          PRIMARY KEY DEFAULT gen_random_uuid(),
    organization_id UUID          NOT NULL REFERENCES organizations(id),
    campaign_id     UUID          NOT NULL REFERENCES campaigns(id),
    title           VARCHAR(200)  NOT NULL,
    target_amount   NUMERIC(14,2) NOT NULL DEFAULT 0,
    target_date     DATE,
    status          VARCHAR(30)   NOT NULL DEFAULT 'pending',
    -- audit
    created_at      TIMESTAMPTZ   NOT NULL DEFAULT now(),
    created_by      UUID          REFERENCES users(id),
    modified_at     TIMESTAMPTZ   NOT NULL DEFAULT now(),
    modified_by     UUID          REFERENCES users(id)
);

-- ============================================================
-- PHASE 1 — Pledges, pledge cards, follow-ups
-- ============================================================

CREATE TABLE pledges (
    id               UUID          PRIMARY KEY DEFAULT gen_random_uuid(),
    organization_id  UUID          NOT NULL REFERENCES organizations(id),
    campaign_id      UUID          NOT NULL REFERENCES campaigns(id),
    donor_id         UUID          NOT NULL REFERENCES donors(id),
    amount           NUMERIC(14,2) NOT NULL CHECK (amount > 0),
    collected_amount NUMERIC(14,2) NOT NULL DEFAULT 0,
    frequency        VARCHAR(30)   NOT NULL DEFAULT 'one_time'
                         CHECK (frequency IN ('one_time','weekly','monthly','quarterly','yearly')),
    payment_method   VARCHAR(50),
    start_date       DATE,
    end_date         DATE,
    status           VARCHAR(30)   NOT NULL DEFAULT 'pending'
                         CHECK (status IN ('pending','active','fulfilled','cancelled','defaulted')),
    source           VARCHAR(50),
    notes            TEXT,
    -- audit
    created_at       TIMESTAMPTZ   NOT NULL DEFAULT now(),
    created_by       UUID          REFERENCES users(id),
    modified_at      TIMESTAMPTZ   NOT NULL DEFAULT now(),
    modified_by      UUID          REFERENCES users(id)
);
CREATE INDEX idx_pledges_org_campaign ON pledges (organization_id, campaign_id);
CREATE INDEX idx_pledges_org_donor    ON pledges (organization_id, donor_id);

CREATE TABLE pledge_schedules (
    id              UUID          PRIMARY KEY DEFAULT gen_random_uuid(),
    organization_id UUID          NOT NULL REFERENCES organizations(id),
    pledge_id       UUID          NOT NULL REFERENCES pledges(id),
    due_date        DATE          NOT NULL,
    amount_due      NUMERIC(14,2) NOT NULL CHECK (amount_due > 0),
    status          VARCHAR(30)   NOT NULL DEFAULT 'pending'
                        CHECK (status IN ('pending','paid','overdue','cancelled')),
    -- audit
    created_at      TIMESTAMPTZ   NOT NULL DEFAULT now(),
    created_by      UUID          REFERENCES users(id),
    modified_at     TIMESTAMPTZ   NOT NULL DEFAULT now(),
    modified_by     UUID          REFERENCES users(id)
);
CREATE INDEX idx_pledge_schedules_pledge ON pledge_schedules (pledge_id);

CREATE TABLE pledge_cards (
    id                  UUID          PRIMARY KEY DEFAULT gen_random_uuid(),
    organization_id     UUID          NOT NULL REFERENCES organizations(id),
    campaign_id         UUID          REFERENCES campaigns(id),
    donor_id            UUID          REFERENCES donors(id),
    image_url           TEXT,
    extracted_json      JSONB,
    amount              NUMERIC(14,2),
    payment_method      VARCHAR(50),
    notes               TEXT,
    verification_status VARCHAR(30)   NOT NULL DEFAULT 'pending'
                            CHECK (verification_status IN ('pending','reviewed','approved','rejected')),
    -- audit
    created_at          TIMESTAMPTZ   NOT NULL DEFAULT now(),
    created_by          UUID          REFERENCES users(id),
    modified_at         TIMESTAMPTZ   NOT NULL DEFAULT now(),
    modified_by         UUID          REFERENCES users(id)
);
CREATE INDEX idx_pledge_cards_org ON pledge_cards (organization_id);

CREATE TABLE donor_assignments (
    id               UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
    organization_id  UUID        NOT NULL REFERENCES organizations(id),
    donor_id         UUID        NOT NULL REFERENCES donors(id),
    ambassador_user_id UUID      NOT NULL REFERENCES users(id),
    campaign_id      UUID        REFERENCES campaigns(id),
    status           VARCHAR(30) NOT NULL DEFAULT 'active'
                         CHECK (status IN ('active','completed','released')),
    UNIQUE (donor_id, campaign_id, ambassador_user_id),
    -- audit
    created_at       TIMESTAMPTZ NOT NULL DEFAULT now(),
    created_by       UUID        REFERENCES users(id),
    modified_at      TIMESTAMPTZ NOT NULL DEFAULT now(),
    modified_by      UUID        REFERENCES users(id)
);
CREATE INDEX idx_donor_assignments_ambassador ON donor_assignments (organization_id, ambassador_user_id);

CREATE TABLE follow_ups (
    id                 UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
    organization_id    UUID        NOT NULL REFERENCES organizations(id),
    donor_id           UUID        NOT NULL REFERENCES donors(id),
    campaign_id        UUID        REFERENCES campaigns(id),
    assigned_to_user_id UUID       REFERENCES users(id),
    due_at             TIMESTAMPTZ,
    status             VARCHAR(30) NOT NULL DEFAULT 'open'
                           CHECK (status IN ('open','completed','cancelled','overdue')),
    outcome            VARCHAR(40),
    notes              TEXT,
    -- audit
    created_at         TIMESTAMPTZ NOT NULL DEFAULT now(),
    created_by         UUID        REFERENCES users(id),
    modified_at        TIMESTAMPTZ NOT NULL DEFAULT now(),
    modified_by        UUID        REFERENCES users(id)
);
CREATE INDEX idx_follow_ups_org_status ON follow_ups (organization_id, status);
CREATE INDEX idx_follow_ups_assignee   ON follow_ups (assigned_to_user_id);

-- ============================================================
-- PHASE 3 — Finance (payments & receipts)
-- ============================================================

CREATE TABLE payments (
    id              UUID          PRIMARY KEY DEFAULT gen_random_uuid(),
    organization_id UUID          NOT NULL REFERENCES organizations(id),
    pledge_id       UUID          NOT NULL REFERENCES pledges(id),
    donor_id        UUID          NOT NULL REFERENCES donors(id),
    amount          NUMERIC(14,2) NOT NULL CHECK (amount > 0),
    payment_method  VARCHAR(50),
    payment_date    DATE          NOT NULL DEFAULT CURRENT_DATE,
    reference       VARCHAR(200),
    notes           TEXT,
    recorded_by     UUID          REFERENCES users(id),
    -- audit
    created_at      TIMESTAMPTZ   NOT NULL DEFAULT now(),
    created_by      UUID          REFERENCES users(id),
    modified_at     TIMESTAMPTZ   NOT NULL DEFAULT now(),
    modified_by     UUID          REFERENCES users(id)
);
CREATE INDEX idx_payments_org    ON payments (organization_id, created_at DESC);
CREATE INDEX idx_payments_pledge ON payments (pledge_id);
CREATE INDEX idx_payments_donor  ON payments (organization_id, donor_id);

CREATE TABLE receipts (
    id              UUID          PRIMARY KEY DEFAULT gen_random_uuid(),
    organization_id UUID          NOT NULL REFERENCES organizations(id),
    payment_id      UUID          NOT NULL REFERENCES payments(id),
    receipt_number  VARCHAR(50)   NOT NULL,
    issued_to       VARCHAR(200)  NOT NULL,
    amount          NUMERIC(14,2) NOT NULL,
    issued_at       TIMESTAMPTZ   NOT NULL DEFAULT now(),
    issued_by       UUID          REFERENCES users(id),
    UNIQUE (organization_id, receipt_number),
    -- audit
    created_at      TIMESTAMPTZ   NOT NULL DEFAULT now(),
    created_by      UUID          REFERENCES users(id),
    modified_at     TIMESTAMPTZ   NOT NULL DEFAULT now(),
    modified_by     UUID          REFERENCES users(id)
);
CREATE INDEX idx_receipts_org ON receipts (organization_id, issued_at DESC);

-- ============================================================
-- PHASE 4 — Events & Volunteers
-- ============================================================

CREATE TABLE events (
    id              UUID         PRIMARY KEY DEFAULT gen_random_uuid(),
    organization_id UUID         NOT NULL REFERENCES organizations(id),
    campaign_id     UUID         REFERENCES campaigns(id),
    name            VARCHAR(200) NOT NULL,
    description     TEXT,
    event_type      VARCHAR(50)  NOT NULL DEFAULT 'general',
    location        VARCHAR(300),
    starts_at       TIMESTAMPTZ,
    ends_at         TIMESTAMPTZ,
    capacity        INTEGER,
    status          VARCHAR(30)  NOT NULL DEFAULT 'draft'
                        CHECK (status IN ('draft','published','in_progress','completed','cancelled')),
    -- audit
    created_at      TIMESTAMPTZ  NOT NULL DEFAULT now(),
    created_by      UUID         REFERENCES users(id),
    modified_at     TIMESTAMPTZ  NOT NULL DEFAULT now(),
    modified_by     UUID         REFERENCES users(id)
);
CREATE INDEX idx_events_org        ON events (organization_id);
CREATE INDEX idx_events_org_starts ON events (organization_id, starts_at);

CREATE TABLE event_registrations (
    id              UUID         PRIMARY KEY DEFAULT gen_random_uuid(),
    organization_id UUID         NOT NULL REFERENCES organizations(id),
    event_id        UUID         NOT NULL REFERENCES events(id),
    donor_id        UUID         REFERENCES donors(id),
    guest_name      VARCHAR(200) NOT NULL,
    guest_email     VARCHAR(255),
    guest_phone     VARCHAR(30),
    party_size      INTEGER      NOT NULL DEFAULT 1 CHECK (party_size > 0),
    status          VARCHAR(30)  NOT NULL DEFAULT 'registered'
                        CHECK (status IN ('registered','checked_in','cancelled','no_show')),
    check_in_code   VARCHAR(40)  NOT NULL,
    checked_in_at   TIMESTAMPTZ,
    checked_in_by   UUID         REFERENCES users(id),
    notes           TEXT,
    -- audit
    created_at      TIMESTAMPTZ  NOT NULL DEFAULT now(),
    created_by      UUID         REFERENCES users(id),
    modified_at     TIMESTAMPTZ  NOT NULL DEFAULT now(),
    modified_by     UUID         REFERENCES users(id)
);
CREATE INDEX idx_event_registrations_event ON event_registrations (event_id);
CREATE INDEX idx_event_registrations_org   ON event_registrations (organization_id);
CREATE UNIQUE INDEX uq_event_registrations_code ON event_registrations (event_id, check_in_code);

CREATE TABLE volunteer_shifts (
    id              UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
    organization_id UUID        NOT NULL REFERENCES organizations(id),
    event_id        UUID        NOT NULL REFERENCES events(id),
    title           VARCHAR(200) NOT NULL,
    description     TEXT,
    starts_at       TIMESTAMPTZ,
    ends_at         TIMESTAMPTZ,
    slots           INTEGER     NOT NULL DEFAULT 1 CHECK (slots > 0),
    status          VARCHAR(30) NOT NULL DEFAULT 'open'
                        CHECK (status IN ('open','full','closed','cancelled')),
    -- audit
    created_at      TIMESTAMPTZ NOT NULL DEFAULT now(),
    created_by      UUID        REFERENCES users(id),
    modified_at     TIMESTAMPTZ NOT NULL DEFAULT now(),
    modified_by     UUID        REFERENCES users(id)
);
CREATE INDEX idx_volunteer_shifts_event ON volunteer_shifts (event_id);
CREATE INDEX idx_volunteer_shifts_org   ON volunteer_shifts (organization_id);

CREATE TABLE volunteer_assignments (
    id              UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
    organization_id UUID        NOT NULL REFERENCES organizations(id),
    shift_id        UUID        NOT NULL REFERENCES volunteer_shifts(id),
    user_id         UUID        NOT NULL REFERENCES users(id),
    status          VARCHAR(30) NOT NULL DEFAULT 'assigned'
                        CHECK (status IN ('assigned','confirmed','checked_in','completed','cancelled')),
    checked_in_at   TIMESTAMPTZ,
    UNIQUE (shift_id, user_id),
    -- audit
    created_at      TIMESTAMPTZ NOT NULL DEFAULT now(),
    created_by      UUID        REFERENCES users(id),
    modified_at     TIMESTAMPTZ NOT NULL DEFAULT now(),
    modified_by     UUID        REFERENCES users(id)
);
CREATE INDEX idx_volunteer_assignments_shift ON volunteer_assignments (shift_id);
CREATE INDEX idx_volunteer_assignments_user  ON volunteer_assignments (organization_id, user_id);

